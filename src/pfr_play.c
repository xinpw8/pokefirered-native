static bool8 EnsureDirectoryPath(const char *path)
{
    char scratch[PATH_MAX];
    size_t len;
    size_t i;

    if (path == NULL || path[0] == '\0')
        return FALSE;

    len = strlen(path);
    if (len >= sizeof(scratch))
        return FALSE;

    snprintf(scratch, sizeof(scratch), "%s", path);
    for (i = 1; i < len; i++)
    {
        if (scratch[i] != '/')
            continue;

        scratch[i] = '\0';
        if (scratch[0] != '\0' && mkdir(scratch, 0775) != 0 && errno != EEXIST)
            return FALSE;
        scratch[i] = '/';
    }

    if (mkdir(scratch, 0775) != 0 && errno != EEXIST)
        return FALSE;

    return TRUE;
}

static bool8 EnsureParentDirectoryForFile(const char *path)
{
    char scratch[PATH_MAX];
    char *slash;

    if (path == NULL || path[0] == '\0')
        return FALSE;

    snprintf(scratch, sizeof(scratch), "%s", path);
    slash = strrchr(scratch, '/');
    if (slash == NULL)
        return TRUE;

    if (slash == scratch)
        slash[1] = '\0';
    else
        *slash = '\0';

    return EnsureDirectoryPath(scratch);
}

static bool8 CopyFileToPath(const char *srcPath, const char *dstPath, bool8 overwrite)
{
    FILE *src = NULL;
    FILE *dst = NULL;
    char tmpPath[PATH_MAX];
    unsigned char buffer[8192];
    size_t bytesRead;
    bool8 ok = FALSE;

    if (srcPath == NULL || dstPath == NULL || srcPath[0] == '\0' || dstPath[0] == '\0')
        return FALSE;

    if (strcmp(srcPath, dstPath) == 0)
        return TRUE;

    if (!overwrite && access(dstPath, F_OK) == 0)
        return FALSE;

    if (!EnsureParentDirectoryForFile(dstPath))
        return FALSE;

    src = fopen(srcPath, "rb");
    if (src == NULL)
        return FALSE;

    snprintf(tmpPath, sizeof(tmpPath), "%s.tmp", dstPath);
    dst = fopen(tmpPath, "wb");
    if (dst == NULL)
        goto cleanup;

    while ((bytesRead = fread(buffer, 1, sizeof(buffer), src)) != 0)
    {
        if (fwrite(buffer, 1, bytesRead, dst) != bytesRead)
            goto cleanup;
    }

    if (ferror(src))
        goto cleanup;

    if (fclose(dst) != 0)
    {
        dst = NULL;
        goto cleanup;
    }
    dst = NULL;

    if (overwrite)
    {
        if (rename(tmpPath, dstPath) != 0)
            goto cleanup;
    }
    else
    {
        if (link(tmpPath, dstPath) != 0)
            goto cleanup;
        if (unlink(tmpPath) != 0)
        {
            unlink(dstPath);
            goto cleanup;
        }
    }

    ok = TRUE;

cleanup:
    if (dst != NULL)
        fclose(dst);
    if (!ok)
        unlink(tmpPath);
    if (src != NULL)
        fclose(src);
    return ok;
}

static bool8 BuildChildPath(char *outPath, size_t outSize, const char *dirPath, const char *name)
{
    size_t dirLen;
    size_t nameLen;

    if (outPath == NULL || outSize == 0 || dirPath == NULL || name == NULL)
        return FALSE;

    dirLen = strlen(dirPath);
    nameLen = strlen(name);
    if (dirLen == 0 || dirLen + 1 + nameLen + 1 > outSize)
        return FALSE;

    memcpy(outPath, dirPath, dirLen);
    outPath[dirLen] = '/';
    memcpy(outPath + dirLen + 1, name, nameLen + 1);
    return TRUE;
}

static char *TrimAsciiWhitespace(char *text)
{
    char *end;

    while (*text == ' ' || *text == '\t' || *text == '\r' || *text == '\n')
        text++;

    end = text + strlen(text);
    while (end > text
        && (end[-1] == ' ' || end[-1] == '\t' || end[-1] == '\r' || end[-1] == '\n'))
    {
        *--end = '\0';
    }

    return text;
}

static bool8 BuildSnapshotPath(char *outPath, size_t outSize, u32 frame, const char *label)
{
    time_t now;
    struct tm tmNow;
    char safeLabel[96];

    if (!EnsureDirectoryPath(sSnapshotDir))
        return FALSE;

    now = time(NULL);
    if (localtime_r(&now, &tmNow) == NULL)
        return FALSE;

    if (label != NULL && label[0] != '\0')
    {
        snprintf(safeLabel, sizeof(safeLabel), "%.95s", label);
        SanitizeSnapshotLabel(safeLabel);
        snprintf(outPath, outSize,
                 "%s/%04d%02d%02d_%02d%02d%02d_frame%06u_%s.sav",
                 sSnapshotDir,
                 tmNow.tm_year + 1900,
                 tmNow.tm_mon + 1,
                 tmNow.tm_mday,
                 tmNow.tm_hour,
                 tmNow.tm_min,
                 tmNow.tm_sec,
                 frame,
                 safeLabel);
    }
    else
    {
        snprintf(outPath, outSize,
                 "%s/%04d%02d%02d_%02d%02d%02d_frame%06u.sav",
                 sSnapshotDir,
                 tmNow.tm_year + 1900,
                 tmNow.tm_mon + 1,
                 tmNow.tm_mday,
                 tmNow.tm_hour,
                 tmNow.tm_min,
                 tmNow.tm_sec,
                 frame);
    }
    return TRUE;
}

static bool8 FindLatestSnapshotPath(char *outPath, size_t outSize)
{
    DIR *dir;
    struct dirent *entry;
    char path[PATH_MAX];
    struct stat st;
    time_t bestTime = 0;
    bool8 found = FALSE;

    dir = opendir(sSnapshotDir);
    if (dir == NULL)
        return FALSE;

    while ((entry = readdir(dir)) != NULL)
    {
        size_t nameLen = strlen(entry->d_name);

        if (nameLen < 4 || strcmp(entry->d_name + nameLen - 4, ".sav") != 0)
            continue;
        if (!BuildChildPath(path, sizeof(path), sSnapshotDir, entry->d_name))
            continue;

        if (stat(path, &st) != 0 || !S_ISREG(st.st_mode))
            continue;

        if (!found || st.st_mtime > bestTime)
        {
            bestTime = st.st_mtime;
            snprintf(outPath, outSize, "%s", path);
            found = TRUE;
        }
    }

    closedir(dir);
    return found;
}

static void LogAvailableSnapshots(u32 frame)
{
    DIR *dir;
    struct dirent *entry;
    char path[PATH_MAX];
    char buffer[PATH_MAX + 64];
    u32 count = 0;

    dir = opendir(sSnapshotDir);
    if (dir == NULL)
    {
        snprintf(buffer, sizeof(buffer), "snapshot list: directory unavailable (%s)", sSnapshotDir);
        TraceLog(frame, buffer);
        return;
    }

    while ((entry = readdir(dir)) != NULL)
    {
        size_t nameLen = strlen(entry->d_name);

        if (nameLen < 4 || strcmp(entry->d_name + nameLen - 4, ".sav") != 0)
            continue;
        if (!BuildChildPath(path, sizeof(path), sSnapshotDir, entry->d_name))
            continue;

        snprintf(buffer, sizeof(buffer), "snapshot[%u]: %s", count, path);
        TraceLog(frame, buffer);
        count++;
    }

    if (count == 0)
        TraceLog(frame, "snapshot list: no .sav files found");

    closedir(dir);
}

