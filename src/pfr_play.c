static void crash_handler(int sig, siginfo_t *info, void *ucontext)
{
    void *frames[32];
    int logFd;
    int n = backtrace(frames, 32);
    fprintf(stderr, "\n=== CRASH: signal %d (%s) fault_addr=%p si_code=%d ===\n", sig, sig==11?"SIGSEGV":sig==7?"SIGBUS":"other", info->si_addr, info->si_code);
    backtrace_symbols_fd(frames, n, STDERR_FILENO);
    logFd = HostLogGetFd();
    if (logFd >= 0 && logFd != STDERR_FILENO)
    {
        dprintf(logFd, "\n=== CRASH: signal %d ===\n", sig);
        backtrace_symbols_fd(frames, n, logFd);
        fsync(logFd);
    }
    _exit(128 + sig);
}

int main(int argc, char *argv[])
{
    struct timespec benchmarkStartTime;
    struct timespec benchmarkEndTime;
    bool8 benchmarkClockStarted = FALSE;
    bool8 benchmarkClockStopped = FALSE;
    double benchmarkElapsedSeconds = 0.0;

    {
        struct sigaction sa;
        memset(&sa, 0, sizeof(sa));
        sa.sa_sigaction = crash_handler;
        sa.sa_flags = SA_SIGINFO;
        sigaction(SIGSEGV, &sa, NULL);
        sigaction(SIGBUS, &sa, NULL);
        sigaction(SIGABRT, &sa, NULL);
    }

    int i;
    int argi;
    const char *scriptPath = NULL;
    const char *autoPlayOak = getenv("PFR_AUTOPLAY_OAK_NEWGAME");
    const char *traceNamingFrames = getenv("PFR_TRACE_NAMING_FRAMES");
    const char *traceOverworldStartup = getenv("PFR_TRACE_OVERWORLD_STARTUP");

    sAutoPlayOakNewGameEnabled = (autoPlayOak != NULL
                               && autoPlayOak[0] != '\0'
                               && strcmp(autoPlayOak, "0") != 0);
    sTraceNamingFramesEnabled = (traceNamingFrames != NULL
                              && traceNamingFrames[0] != '\0'
                              && strcmp(traceNamingFrames, "0") != 0);
    sTraceOverworldStartupEnabled = (traceOverworldStartup != NULL
                                  && traceOverworldStartup[0] != '\0'
                                  && strcmp(traceOverworldStartup, "0") != 0);
    const char *debugGiveStarter = getenv("PFR_DEBUG_GIVE_STARTER");
    sDebugGiveStarterEnabled = (debugGiveStarter != NULL
                             && debugGiveStarter[0] != '\0'
                             && strcmp(debugGiveStarter, "0") != 0);
    ResetAutoPlayOakState();
    sTraceOverworldStartupActive = FALSE;
    sTraceOverworldStartupCompleted = FALSE;
    sTraceOverworldFirstVisibleLogged = FALSE;
    sTraceOverworldStartupFrame = 0;
    sProgramArgv = argv;
    SetPathOption(sActiveSavePath, sizeof(sActiveSavePath), getenv("PFR_SAVE_PATH"), "pokefirered.sav");
    SetPathOption(sBootLoadSavePath, sizeof(sBootLoadSavePath), getenv("PFR_LOAD_SAVE"), NULL);
    SetPathOption(sSnapshotDir, sizeof(sSnapshotDir), getenv("PFR_SNAPSHOT_DIR"), "pfr_debug_saves");
    SetPathOption(sStateDir, sizeof(sStateDir), getenv("PFR_STATE_DIR"), "pfr_debug_states");
    SetPathOption(sQuickLoadPath, sizeof(sQuickLoadPath), getenv("PFR_QUICKLOAD_PATH"), NULL);
    SetPathOption(sControlFilePath, sizeof(sControlFilePath), getenv("PFR_CONTROL_FILE"), NULL);
    sAutoPlayContinueEnabled = FlagEnabled(getenv("PFR_AUTOPLAY_CONTINUE"));
    sDisableAudioEnabled = FlagEnabled(getenv("PFR_DISABLE_AUDIO"));
    if (scriptPath == NULL)
        scriptPath = getenv("PFR_INPUT_SCRIPT");

    for (argi = 1; argi < argc; argi++)
    {
        if (strcmp(argv[argi], "--input") == 0 && argi + 1 < argc)
        {
            scriptPath = argv[++argi];
        }
        else if (strcmp(argv[argi], "--save-path") == 0 && argi + 1 < argc)
        {
            SetPathOption(sActiveSavePath, sizeof(sActiveSavePath), argv[++argi], NULL);
        }
        else if (strcmp(argv[argi], "--load-save") == 0 && argi + 1 < argc)
        {
            SetPathOption(sBootLoadSavePath, sizeof(sBootLoadSavePath), argv[++argi], NULL);
        }
        else if (strcmp(argv[argi], "--snapshot-dir") == 0 && argi + 1 < argc)
        {
            SetPathOption(sSnapshotDir, sizeof(sSnapshotDir), argv[++argi], NULL);
        }
        else if (strcmp(argv[argi], "--state-dir") == 0 && argi + 1 < argc)
        {
            SetPathOption(sStateDir, sizeof(sStateDir), argv[++argi], NULL);
        }
        else if (strcmp(argv[argi], "--quickload-path") == 0 && argi + 1 < argc)
        {
            SetPathOption(sQuickLoadPath, sizeof(sQuickLoadPath), argv[++argi], NULL);
        }
        else if (strcmp(argv[argi], "--control-file") == 0 && argi + 1 < argc)
        {
            SetPathOption(sControlFilePath, sizeof(sControlFilePath), argv[++argi], NULL);
        }
        else if (strcmp(argv[argi], "--headless") == 0)
        {
            setenv("PFR_HEADLESS", "1", 1);
        }
        else if (strcmp(argv[argi], "--unthrottled") == 0)
        {
            setenv("PFR_UNTHROTTLED", "1", 1);
        }
        else if (strcmp(argv[argi], "--fast-forward") == 0 && argi + 1 < argc)
        {
            u32 fastForward;

            if (!ParseU32Option(argv[argi + 1], &fastForward))
            {
                fprintf(stderr, "pfr_play: invalid fast-forward factor %s\n", argv[argi + 1]);
                return 1;
            }
            setenv("PFR_UNTHROTTLED", "1", 1);
            setenv("PFR_FAST_FORWARD_FACTOR", argv[++argi], 1);
        }
        else if (strcmp(argv[argi], "--benchmark-frames") == 0 && argi + 1 < argc)
        {
            if (!ParseU32Option(argv[argi + 1], &sBenchmarkFrameLimit) || sBenchmarkFrameLimit == 0)
            {
                fprintf(stderr, "pfr_play: invalid benchmark frame count %s\n", argv[argi + 1]);
                return 1;
            }
            sBenchmarkEnabled = TRUE;
            argi++;
        }
        else if (strcmp(argv[argi], "--mute") == 0)
        {
            sDisableAudioEnabled = TRUE;
        }
        else if (strcmp(argv[argi], "--max-speed") == 0)
        {
            setenv("PFR_HEADLESS", "1", 1);
            setenv("PFR_UNTHROTTLED", "1", 1);
            setenv("PFR_MAX_SPEED", "1", 1);
            if (getenv("PFR_FAST_FORWARD_FACTOR") == NULL)
                setenv("PFR_FAST_FORWARD_FACTOR", "4096", 1);
            sDisableAudioEnabled = TRUE;
        }
        else if (strcmp(argv[argi], "--autoplay-continue") == 0)
        {
            sAutoPlayContinueEnabled = TRUE;
        }
        else if (strcmp(argv[argi], "--autoplay-oak") == 0)
        {
            sAutoPlayOakNewGameEnabled = TRUE;
        }
        else if (strcmp(argv[argi], "--rl-instrument") == 0)
        {
            sRlInstrumentEnabled = TRUE;
            sRlInstrumentLog = fopen("rl_instrument.log", "w");
            if (!sRlInstrumentLog)
            {
                fprintf(stderr, "pfr_play: could not open rl_instrument.log\n");
                return 1;
            }
            fprintf(sRlInstrumentLog, "# RL Action Boundary Instrumentation\n");
            fprintf(sRlInstrumentLog, "# Play the game normally. Each line logs a player_ready() transition.\n");
            fflush(sRlInstrumentLog);
        }
        else if (strcmp(argv[argi], "--help") == 0)
        {
            fprintf(stderr,
                    "usage: %s [input_script] [--input path] [--save-path path] [--load-save path]\n"
                    "          [--snapshot-dir path] [--state-dir path] [--quickload-path path] [--control-file path]\n"
                    "          [--headless] [--unthrottled] [--fast-forward n] [--benchmark-frames n] [--mute] [--max-speed]\n"
                    "          [--autoplay-continue] [--autoplay-oak] [--rl-instrument]\n",
                    argv[0]);
            return 0;
        }
        else if (argv[argi][0] != '-' && scriptPath == NULL)
        {
            scriptPath = argv[argi];
        }
        else if (argv[argi][0] == '-')
        {
            fprintf(stderr, "pfr_play: unknown or incomplete option %s\n", argv[argi]);
            return 1;
        }
    }

    if (!EnsureParentDirectoryForFile(sActiveSavePath))
    {
        fprintf(stderr, "pfr_play: could not create parent directory for %s\n", sActiveSavePath);
        return 1;
    }
    if (!EnsureDirectoryPath(sSnapshotDir))
    {
        fprintf(stderr, "pfr_play: could not create snapshot dir %s\n", sSnapshotDir);
        return 1;
    }
    if (!EnsureDirectoryPath(sStateDir))
    {
        fprintf(stderr, "pfr_play: could not create state dir %s\n", sStateDir);
        return 1;
    }

    sFastModeEnabled = FlagEnabled(getenv("PFR_MAX_SPEED"));

    if (sBootLoadSavePath[0] != '\0' && !CopyFileToPath(sBootLoadSavePath, sActiveSavePath, TRUE))
    {
        fprintf(stderr, "pfr_play: failed to load save %s into %s\n", sBootLoadSavePath, sActiveSavePath);
        return 1;
    }

    /* Initialize host subsystems */
    HostMemoryInit();
    HostRendererInit();
    mkdir("pfr_play_frames", 0775);
    sTraceFile = fopen("pfr_play_trace.log", "w");
    if (sTraceFile != NULL)
    {
        setvbuf(sTraceFile, NULL, _IOLBF, 0);
        HostLogAttachFile(sTraceFile);
    }

    if (!HostDisplayInit())
    {
        HostLogPrintf("pfr_play: Failed to initialize display (SDL2 required)\n");
        return 1;
    }
    HostDisplaySetStateDir(sStateDir);

    /* Boot chain */
    HostCrt0Init();
    REG_KEYINPUT = KEYS_MASK; /* all keys released */

    /* ── AgbMain initialization sequence ── */

    InitGpuRegManager();

    /* Interrupt dispatch table */
    for (i = 0; i < 14; i++)
        gIntrTable[i] = IntrDummy;
    gIntrTable[3] = PlayHBlankHandler;
    gIntrTable[4] = PlayVBlankHandler;

    REG_IME = 1;
    REG_IE |= INTR_FLAG_VBLANK;

    InitKeys();

    /* Full native sound init: sets up SOUND_INFO_PTR, all SoundInfo fields,
     * music players, and cry players — without DMA/timer hardware writes. */
    HostNativeSoundInit();
    if (!sDisableAudioEnabled)
        HostAudioInit();
    else
        TraceLog(0, "audio disabled by debug configuration");

    EnableVCountIntrAtLine150();
    /* InitRFU() skipped: spins on rfu_waitREQComplete() waiting for
     * wireless adapter hardware response. No wireless adapter on native.
     * Single-player gameplay does not use RFU. */
    HostFlashInit(sActiveSavePath);
    CheckForFlashMemory();
    HostSavestateInit();

    /* Protect host-side resource pointers from savestate restore.
     * Savestate restore does a bulk memcpy of .data/.bss, which
     * would overwrite these with stale values from a prior session.
     * Any pointer to heap, libc, or OS resources MUST be protected. */
    HostSavestateProtectRegion(&sTraceFile, sizeof(sTraceFile));
    HostSavestateProtectRegion(&sRlInstrumentLog, sizeof(sRlInstrumentLog));
    HostSavestateProtectRegion(&sSymTable, sizeof(sSymTable));
    HostSavestateProtectRegion(&sSymCount, sizeof(sSymCount));
    HostSavestateProtectRegion(&sProgramArgv, sizeof(sProgramArgv));

    /* InitMainCallbacks is static in main.c; replicate inline: */
    gMain.vblankCounter1 = 0;
    gMain.vblankCounter2 = 0;
    gMain.callback1 = NULL;
    gMain.callback2 = NULL;
    gMain.vblankCallback = NULL;
    gMain.state = 0;
    gSaveBlock2Ptr = &gSaveBlock2;
    gSaveBlock1Ptr = &gSaveBlock1;
    gPokemonStoragePtr = &gPokemonStorage;
    gSaveBlock2.encryptionKey = 0;
    gQuestLogPlaybackState = QL_PLAYBACK_STATE_STOPPED;

    InitMapMusic();
    ClearDma3Requests();
    ResetBgs();
    InitHeap(gHeap, HEAP_SIZE);
    SetDefaultFontsPointer();

    gSoftResetDisabled = FALSE;
    gHelpSystemEnabled = FALSE;
    SetNotInSaveFailedScreen();

    gHostIntroStubLoadGameSaveResult = SAVE_STATUS_EMPTY;
    HostNewGameStubReset();
    HostOakSpeechStubReset();

    /* Patch 32-bit LE pointer fields in generated script bytecode arrays.
     * Must run before any game code that reads script pointers. */
    HostPatchBattleScriptPointers();
    HostPatchEventScriptPointers();
    HostPatchFieldEffectScriptPointers();
    HostPatchBattleAIScriptPointers();
    HostPatchBattleAnimScriptPointers();

    HostIntroStubReset();
    HostTitleScreenStubReset();
    sLastCallback1 = (MainCallback)(uintptr_t)-1;
    sLastCallback2 = (MainCallback)(uintptr_t)-1;
    sLastUniformColor = 0;
    sUniformFrameRunLength = 0;
    sScriptedInputEnabled = FALSE;
    sScriptedInputRangeCount = 0;
    InitSymbolTable();
    TraceLog(0, "pfr_play trace started");
    {
        char buffer[PATH_MAX + 64];

        snprintf(buffer, sizeof(buffer), "active save path: %s", sActiveSavePath);
        TraceLog(0, buffer);
        snprintf(buffer, sizeof(buffer), "snapshot dir: %s", sSnapshotDir);
        TraceLog(0, buffer);
        snprintf(buffer, sizeof(buffer), "state dir: %s", sStateDir);
        TraceLog(0, buffer);
        if (sBootLoadSavePath[0] != '\0')
        {
            snprintf(buffer, sizeof(buffer), "boot load save: %s", sBootLoadSavePath);
            TraceLog(0, buffer);
        }
        if (sQuickLoadPath[0] != '\0')
        {
            snprintf(buffer, sizeof(buffer), "quickload path: %s", sQuickLoadPath);
            TraceLog(0, buffer);
        }
        if (sControlFilePath[0] != '\0')
        {
            snprintf(buffer, sizeof(buffer), "control file: %s", sControlFilePath);
            TraceLog(0, buffer);
        }
        snprintf(buffer, sizeof(buffer), "fast-forward factor: %u", HostDisplayGetFastForwardFactor());
        TraceLog(0, buffer);
        if (sBenchmarkEnabled)
        {
            snprintf(buffer, sizeof(buffer), "benchmark frame limit: %u", sBenchmarkFrameLimit);
            TraceLog(0, buffer);
        }
    }
    if (sAutoPlayOakNewGameEnabled)
        TraceLog(0, "autoplay oak-newgame enabled");
    if (sAutoPlayContinueEnabled)
        TraceLog(0, "autoplay continue enabled");
    if (sTraceNamingFramesEnabled)
        TraceLog(0, "trace naming frames enabled");
    if (sTraceOverworldStartupEnabled)
        TraceLog(0, "trace overworld startup enabled");
    if (sFastModeEnabled)
        TraceLog(0, "max-speed mode enabled: heavy debug snapshots suppressed");

    if (scriptPath != NULL)
    {
        char buffer[160];

        if (!LoadScriptedInputFile(scriptPath))
            return 1;
        snprintf(buffer, sizeof(buffer),
                 "loaded scripted input file %s with %u ranges",
                 scriptPath,
                 sScriptedInputRangeCount);
        TraceLog(0, buffer);
    }

    {
        extern void CB2_InitCopyrightScreenAfterBootup(void);
        SetMainCallback2(CB2_InitCopyrightScreenAfterBootup);
    }

    /* ── Main loop ── */

    if (sBenchmarkEnabled && clock_gettime(CLOCK_MONOTONIC, &benchmarkStartTime) == 0)
        benchmarkClockStarted = TRUE;

    for (;;)
    {
        u32 burst = HostDisplayGetFastForwardFactor();
        u32 stepsThisBurst;
        u32 step;
        bool8 renderFrame = HostDisplayNeedsFrameRender();
        bool8 modalOverlayActive = HostDisplayHasModalOverlay();

        if (burst == 0)
            burst = 1;
        stepsThisBurst = burst;
        if (sBenchmarkEnabled)
        {
            u32 remainingFrames;

            if (sEmulatedFrameCount >= sBenchmarkFrameLimit)
            {
                sBenchmarkCompleted = TRUE;
                break;
            }

            remainingFrames = sBenchmarkFrameLimit - sEmulatedFrameCount;
            if (stepsThisBurst > remainingFrames)
                stepsThisBurst = remainingFrames;
        }

        if (!sPresentRestoredFramePending && !modalOverlayActive)
        {
            for (step = 0; step < stepsThisBurst; step++)
            {
                bool8 renderThisStep = renderFrame && (step + 1 == stepsThisBurst);
                StepFrame(renderThisStep);
            }
        }
        else
        {
            sPresentRestoredFramePending = FALSE;
        }

        /* HostDisplayPresent: render → present → poll input → vsync pacing.
         * Returns FALSE on ESC / window close. */
        if (!HostDisplayPresent())
            break;

        if (sBenchmarkEnabled && sEmulatedFrameCount >= sBenchmarkFrameLimit)
        {
            sBenchmarkCompleted = TRUE;
            break;
        }

        HandleHostActions(HostDisplayGetFrameCount());
        if (sRestartRequested || sShutdownRequested)
            break;

        if (sSnapshotRequested)
        {
            TraceGpuState(HostDisplayGetFrameCount(), sSnapshotLabel);
            DumpCurrentFramebuffer(HostDisplayGetFrameCount(), sSnapshotLabel);
            sSnapshotRequested = FALSE;
        }

        TraceOverworldStartup(HostDisplayGetFrameCount());

        /* Periodic snapshots at stable frames for debugging */
        if (!sFastModeEnabled)
        {
            u32 fc = HostDisplayGetFrameCount();
            if (fc % 200 == 0 && fc >= 6200 && fc <= 14000 || fc == 13100 || fc == 13200 || fc == 13300 ||
                fc == 13500 || fc == 14000 || fc == 14500 || fc == 15000 ||
                fc == 16000 || fc == 17000 || fc == 18000 || fc == 20000)
            {
                char label[32];
                snprintf(label, sizeof(label), "periodic_%u", fc);
                DumpCurrentFramebuffer(fc, label);
            }
        }

        TraceFramebufferUniformity(HostDisplayGetFrameCount());
    }

    if (sBenchmarkEnabled && benchmarkClockStarted && clock_gettime(CLOCK_MONOTONIC, &benchmarkEndTime) == 0)
    {
        benchmarkElapsedSeconds = TimespecDiffSeconds(&benchmarkStartTime, &benchmarkEndTime);
        benchmarkClockStopped = TRUE;
    }

    if (sRestartRequested && RestartSelf())
        return 0;

    HostAudioShutdown();
    HostDisplayDestroy();
    if (sTraceFile != NULL)
        fclose(sTraceFile);
    if (sBenchmarkCompleted && benchmarkClockStopped)
        PrintBenchmarkSummary(benchmarkElapsedSeconds);
    if (sBenchmarkEnabled && !sBenchmarkCompleted)
        return 1;
    return 0;
}
