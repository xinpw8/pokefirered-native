static bool8 ParseU32Option(const char *value, u32 *outValue)
{
    char *end = NULL;
    unsigned long parsed;

    if (value == NULL || value[0] == '\0')
        return FALSE;

    parsed = strtoul(value, &end, 10);
    if (end == NULL || *end != '\0' || parsed == 0 || parsed > 1000000ul)
        return FALSE;

    *outValue = (u32)parsed;
    return TRUE;
}

