static bool8 StageLoadFromSavePath(u32 frame, const char *action, const char *reason, const char *sourcePath)
{
    char buffer[PATH_MAX * 2 + 128];

    if (sActiveSavePath[0] == '\0' || sourcePath == NULL || sourcePath[0] == '\0')
        return FALSE;

    if (!CopyFileToPath(sourcePath, sActiveSavePath, TRUE))
    {
        snprintf(buffer, sizeof(buffer),
                 "%s (%s) failed copying %s -> %s",
                 action,
                 reason,
                 sourcePath,
                 sActiveSavePath);
        TraceLog(frame, buffer);
        return FALSE;
    }

    setenv("PFR_AUTOPLAY_CONTINUE", "1", 1);
    sRestartRequested = TRUE;
    snprintf(buffer, sizeof(buffer),
             "%s (%s): staged %s -> %s, restarting",
             action,
             reason,
             sourcePath,
             sActiveSavePath);
    TraceLog(frame, buffer);
    return TRUE;
}

